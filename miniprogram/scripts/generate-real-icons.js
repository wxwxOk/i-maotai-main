/**
 * 生成真实的 tabBar 图标 (使用 canvas)
 * 需要先安装: npm install canvas
 * 运行: node scripts/generate-real-icons.js
 */

const fs = require('fs');
const path = require('path');

// 简单的 PNG 生成 (不依赖 canvas)
// 使用预定义的简单图标 base64

// 最小有效 PNG (1x1 灰色像素)
const minPNG = Buffer.from([
  0x89, 0x50, 0x4E, 0x47, 0x0D, 0x0A, 0x1A, 0x0A, // PNG signature
  0x00, 0x00, 0x00, 0x0D, // IHDR length
  0x49, 0x48, 0x44, 0x52, // IHDR
  0x00, 0x00, 0x00, 0x01, // width = 1
  0x00, 0x00, 0x00, 0x01, // height = 1
  0x08, 0x02,             // bit depth = 8, color type = 2 (RGB)
  0x00, 0x00, 0x00,       // compression, filter, interlace
  0x90, 0x77, 0x53, 0xDE, // IHDR CRC
  0x00, 0x00, 0x00, 0x0C, // IDAT length
  0x49, 0x44, 0x41, 0x54, // IDAT
  0x08, 0xD7, 0x63, 0x90, 0x90, 0x90, 0x00, 0x00, // compressed data (gray)
  0x01, 0x84, 0x00, 0xC1, // IDAT CRC
  0x00, 0x00, 0x00, 0x00, // IEND length
  0x49, 0x45, 0x4E, 0x44, // IEND
  0xAE, 0x42, 0x60, 0x82  // IEND CRC
]);

// 蓝色像素 PNG
const bluePNG = Buffer.from([
  0x89, 0x50, 0x4E, 0x47, 0x0D, 0x0A, 0x1A, 0x0A,
  0x00, 0x00, 0x00, 0x0D,
  0x49, 0x48, 0x44, 0x52,
  0x00, 0x00, 0x00, 0x01,
  0x00, 0x00, 0x00, 0x01,
  0x08, 0x02,
  0x00, 0x00, 0x00,
  0x90, 0x77, 0x53, 0xDE,
  0x00, 0x00, 0x00, 0x0C,
  0x49, 0x44, 0x41, 0x54,
  0x08, 0xD7, 0x63, 0x68, 0xD8, 0xFC, 0x00, 0x00, // compressed data (blue #1890ff)
  0x02, 0x0D, 0x01, 0x02,
  0x00, 0x00, 0x00, 0x00,
  0x49, 0x45, 0x4E, 0x44,
  0xAE, 0x42, 0x60, 0x82
]);

const iconsDir = path.join(__dirname, '../assets/icons');

if (!fs.existsSync(iconsDir)) {
  fs.mkdirSync(iconsDir, { recursive: true });
}

// 为了确保图标有效，我们使用最小 PNG 作为占位
const icons = [
  { name: 'home.png', data: minPNG },
  { name: 'home-active.png', data: bluePNG },
  { name: 'list.png', data: minPNG },
  { name: 'list-active.png', data: bluePNG },
  { name: 'user.png', data: minPNG },
  { name: 'user-active.png', data: bluePNG },
];

icons.forEach(icon => {
  const filePath = path.join(iconsDir, icon.name);
  fs.writeFileSync(filePath, icon.data);
  console.log(`Created: ${icon.name}`);
});

console.log('\n占位图标已生成！');
console.log('\n建议从以下网站下载正式图标替换：');
console.log('  1. 阿里巴巴矢量图标库: https://www.iconfont.cn/');
console.log('  2. IconPark: https://iconpark.oceanengine.com/');
console.log('\n图标要求:');
console.log('  - 格式: PNG');
console.log('  - 尺寸: 建议 81x81 像素');
console.log('  - 大小: 不超过 40KB');
